public class OperadoresRelacionales {
    public static void main(String[] args) {

        //operador mayor que >
        boolean esUnoMayorQueDos = 1 > 2;
        System.out.println("esUnoMayorQueDos = " + esUnoMayorQueDos);
        //Operador de igualdad ==
        boolean esTresComaUnoIgualQueTresComaDos= 3.1 == 3.2;
        System.out.println("esTresComaUnoIgualQueTresComaDos = " + esTresComaUnoIgualQueTresComaDos);
    }
}
